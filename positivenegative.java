package Forloopday7;

public class positivenegative {
	public static void main(String[]args) {
		int pos=0;
		int neg=0;
		for(int i=-25;i<=5;i++) {
			if(i>0) {
				pos++;
			}
			else {
				neg++;
			}
			}
		System.out.println("pos count="+pos);
		System.out.println("neg count="+neg);
		
		}
	}


