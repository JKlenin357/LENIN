package Forloopday7;

public class Sample2 {
	public static void main(String[]args) {
		int num=1175;
		String ans="";
		for(int i=2;i<=num;i=i+1) {
			
		if(i*i==num) {
			ans="ok";
			break;
		}
			
		}
		if(ans.equals(ans)) {
			System.out.println(num+"it is true");
			
		}
		else {
			System.out.println(num+"it is false");
			}
	
		
	}
}


